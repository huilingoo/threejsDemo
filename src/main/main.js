import * as THREE from 'three';
import { TextGeometry } from 'three/examples/jsm/geometries/TextGeometry';
import { FontLoader } from 'three/examples/jsm/loaders/FontLoader';

var scene,
    camera,
    renderer;

scene = new THREE.Scene(); // 创建场景
camera = new THREE.PerspectiveCamera(75, window.innerWidth / window.innerHeight, 0.1, 1000); // 创建相机
renderer = new THREE.WebGLRenderer(); // 渲染器
renderer.setClearAlpha(0); // 设置渲染器的透明度

renderer.setSize(window.innerWidth, window.innerHeight); // 设置渲染器 大小
document.body.appendChild(renderer.domElement);

scene.add(camera);

camera.position.z = 800;

var radius = 3,
    segemnt = 20,
    rings = 20,
    R = 300;
// THREE.Group继承自THREE.Object3D对象，并且和THREE.Object3D对象没有任何区别，仅仅是名字上的差异。
var group = new THREE.Group();
var bows = new THREE.Group();


var loader = new FontLoader();
loader.load('./font.json', (font) => {

    for (var i = 0; i < 19; i++) {
        var new_text = new TextGeometry("卉灵", {
            font: font,
            size: 16,
            height: 0.7,
            /*
            这里只定义了最基本的参数
            还有其他的参数
            font: THREE.Font的实例
            size: Float, 字体大小, 默认值为100
            height: Float, 挤出文本的厚度。默认值为50
            curveSegments: Integer, (表示文本的)曲线上点的数量，默认值为12
            bevelEnabled: Boolean, 是否开启斜角，默认为false
            bevelThickness: Float, 文本上斜角的深度，默认值为20
            bevelSize: Float, 斜角与原始文本轮廓之间的延伸距离, 默认值为8
            bevelSegments: Integer, 斜角的分段数, 默认值为3
            */
        });
        var material_text = new THREE.MeshLambertMaterial({ color: '#ff2442' });
        var text_1 = new THREE.Mesh(new_text, material_text);
        // text_1.position.z=-7.4;
        text_1.position.x = R * Math.sin((Math.PI / 18) * i);
        text_1.position.y = R * Math.cos((Math.PI / 18) * i);
       
        group.add(text_1);
    }

    for (var j = 0; j < 36; j++) {
        var bow = group.clone();
        bow.rotation.y = Math.PI * 2 / 36 * j;
        bows.add(bow);
    }

    scene.add(bows);


}, (err) => {
    console.log(err)
})


// 设置光线
const pointLight = new THREE.AmbientLight(0x404040);
pointLight.position.x = 0;
pointLight.position.y = 0;
pointLight.position.z = 1000;
scene.add(pointLight);


var mouseX, mouseY, isMove = false;

render(); //渲染界面  
animation();

function animation() {
    if (!isMove) {
        requestAnimationFrame(animation); //循环调用函数
        bows.rotation.y += Math.PI * 0.001;
        bows.rotation.x += Math.PI * 0.001;
        render(); //渲染界面  
    }
}

function render() {
    renderer.render(scene, camera);
}
// /**
//     鼠标点击
// **/
document.onmousedown = function (e) {
    isMove = true;
    mouseX = e.pageX;
    mouseY = e.pageY;
};
document.onmousemove = function (e) {
    if (isMove) {
        var x = e.pageX;
        var y = e.pageY;
        var _x = x - mouseX;
        var _y = y - mouseY;
        bows.rotation.x += _y * 0.001 * Math.PI;
        bows.rotation.y += _x * 0.001 * Math.PI;
        render();
        mouseX = x;
        mouseY = y;
    }
};
document.onmouseup = function () {
    isMove = false;
    animation();
}